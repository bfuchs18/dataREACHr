  lbc_json <- RJSONIO::toJSON(lbc_list, pretty = TRUE)

  # double check
  if (isFALSE(RJSONIO::isValidJSON(lbc_json, asText = TRUE))){
    print('LBC JSON file may be invalid')
  }

  return(lbc_json)

}
